/**
 * op_add - adds a to b
 * @a: 1st operand
 * @b: 2nd operand
 *
 * Return: result, int
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtracts a - b
 * @a: 1st operand
 * @b: 2nd operand
 *
 * Return: result, int
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies a * b
 * @a: 1st operand
 * @b: 2nd operand
 *
 * Return: result, int
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides a / b
 * @a: 1st operand
 * @b: 2nd operand
 *
 * Return: result, int
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - finds mofulus of a % b
 * @a: 1st operand
 * @b: 2nd operand
 *
 * Return: result, int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
